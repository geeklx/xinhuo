package cn.udesk.model;

public class UploadToken {

    private Object token;
    private Object bucket;
    private Object storage_policy;
    private Object host;
    private Object callback;
    private Object accessid;
    private Object signature;
    private Object policy;
    private Object dir;
    private Object expire;

    public Object getToken() {
        return token;
    }

    public void setToken(Object token) {
        this.token = token;
    }

    public Object getBucket() {
        return bucket;
    }

    public void setBucket(Object bucket) {
        this.bucket = bucket;
    }

    public Object getStorage_policy() {
        return storage_policy;
    }

    public void setStorage_policy(Object storage_policy) {
        this.storage_policy = storage_policy;
    }

    public Object getHost() {
        return host;
    }

    public void setHost(Object host) {
        this.host = host;
    }

    public Object getCallback() {
        return callback;
    }

    public void setCallback(Object callback) {
        this.callback = callback;
    }

    public Object getAccessid() {
        return accessid;
    }

    public void setAccessid(Object accessid) {
        this.accessid = accessid;
    }

    public Object getSignature() {
        return signature;
    }

    public void setSignature(Object signature) {
        this.signature = signature;
    }

    public Object getPolicy() {
        return policy;
    }

    public void setPolicy(Object policy) {
        this.policy = policy;
    }

    public Object getDir() {
        return dir;
    }

    public void setDir(Object dir) {
        this.dir = dir;
    }

    public Object getExpire() {
        return expire;
    }

    public void setExpire(Object expire) {
        this.expire = expire;
    }
}
